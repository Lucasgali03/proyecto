package Modelo;

public class Licencia {
	private int num;
	private String pais;
	private String vencimiento;
	private String imagen;
	
	public Licencia(int num, String pais, String vencimiento, String imagen) {
		this.num = num;
		this.pais = pais;
		this.vencimiento = vencimiento;
		this.imagen = imagen;
	}
}
